type X struct {
	p0, p1 image.Point
	r      image.Rectangle
	want   bool
}

func (x *X) eval(t *testing.T, i int) {
	t.Helper()
	dp := image.Pt(512, 512)
	if have := lineOverRect(x.r, x.p0, x.p1); have != x.want {
		dst := image.NewRGBA(image.Rect(0, 0, 1024, 1024))
		draw.Draw(dst, x.r.Add(dp), image.NewUniform(color.RGBA{255, 0, 255, 255}), image.ZP, draw.Src)
		Line(dst, x.p0.Add(dp), x.p1.Add(dp), 1, image.White, image.ZP)
		draw.Draw(dst, x.r.Add(dp), image.NewUniform(color.RGBA{255, 0, 255, 255}), image.ZP, draw.Src)
		fd, _ := os.Create("bad.png")
		png.Encode(fd, dst)
		fd.Close()
		t.Fatalf("%d: %s->%s in %s: have %v, want %v", i, x.p0, x.p1, x.r, have, x.want)
	}
}
func (x *X) warn(t *testing.T, i int) {
	t.Helper()
	if have := lineOverRect(x.r, x.p0, x.p1); have != x.want {
		t.Logf("%d: %s->%s in %s: have %v, want %v", i, x.p0, x.p1, x.r, have, x.want)
	}
}

func genrand() {
	p := image.Pt
	r := image.Rect
	v := func() int {
		return int(rand.Int31n(2) - 1)
	}
	for i := 0; i < 100; i++ {
		x, y, xx, yy, a, b, c, d := v(), v(), v(), v(), v(), v(), v(), v()
		r := r(x, y, xx, yy)
		p0, p1 := p(a, b), p(c, d)
		and := oc(r, p0) & oc(r, p1)
		fmt.Printf("{p%s,p%s, R{p%s, p%s}, %v},\n", p0, p1, r.Min, r.Max, and != 0)
	}
}
